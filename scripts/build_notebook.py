def build() -> None:
    nb = nbf.v4.new_notebook()
    cells = []
    for kind, src in CELLS:
        # All code cells start with a path import for safety
        if kind == "code":
            full = (
                "from pathlib import Path\nROOT = Path('..').resolve()\n" + src
                if "ROOT" in src
                else src
            )
            cells.append(nbf.v4.new_code_cell(full))
        else:
            cells.append(nbf.v4.new_markdown_cell(src))
    nb["cells"] = cells
    nb["metadata"] = {
        "kernelspec": {"display_name": "Python 3", "language": "python", "name": "python3"},
        "language_info": {"name": "python", "version": "3.11"},
    }
    NB_PATH.write_text(nbf.writes(nb), encoding="utf-8")
    print(f"Wrote {NB_PATH.relative_to(ROOT)}")

    # Execute it so outputs are baked in
    print("Executing notebook...")
    result = subprocess.run(
        [
            "uv",
            "run",
            "jupyter",
            "nbconvert",
            "--to",
            "notebook",
            "--execute",
            "--inplace",
            str(NB_PATH),
        ],
        capture_output=True,
        text=True,
    )
    if result.returncode != 0:
        print("Execution failed:")
        print(result.stderr[-2000:])
        raise SystemExit(1)
    print("Notebook executed with outputs.")


if __name__ == "__main__":
    build()
